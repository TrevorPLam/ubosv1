// Mock job queue - in a real implementation, this would use BullMQ
interface MockJob {
  id: string;
  type: string;
  data: any;
  status: "pending" | "running" | "completed" | "failed";
  createdAt: Date;
  processedAt?: Date;
  error?: string;
}

class MockJobQueue {
  private jobs = new Map<string, MockJob>();

  async add(type: string, data: any): Promise<{ jobId: string }> {
    const jobId = `${type}-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
    const job: MockJob = {
      id: jobId,
      type,
      data,
      status: "pending",
      createdAt: new Date(),
    };
    
    this.jobs.set(jobId, job);
    
    // Process job asynchronously
    this.processJob(jobId);
    
    return { jobId };
  }

  async getJob(jobId: string): Promise<MockJob | null> {
    return this.jobs.get(jobId) || null;
  }

  private async processJob(jobId: string): Promise<void> {
    const job = this.jobs.get(jobId);
    if (!job) return;

    try {
      job.status = "running";
      
      if (job.type === "generate-summary") {
        await this.processSummaryJob(job);
      } else if (job.type === "process-feedback") {
        await this.processFeedbackJob(job);
      }
      
      job.status = "completed";
      job.processedAt = new Date();
    } catch (error) {
      job.status = "failed";
      job.error = error instanceof Error ? error.message : String(error);
    }
  }

  private async processSummaryJob(job: MockJob): Promise<void> {
    const { threadId, forceRegenerate, tenantId } = job.data;
    
    // Get thread messages
    const messages = await db
      .select()
      .from(messagesTable)
      .where(eq(messagesTable.threadId, threadId))
      .orderBy(desc(messagesTable.createdAt));

    if (messages.length === 0) {
      throw new Error("No messages found in thread");
    }

    // Generate summary using mock AI
    const summaryText = await this.generateMockSummary(messages);
    
    // Extract key points and action items
    const keyPoints = this.extractKeyPoints(messages);
    const actionItems = this.extractActionItems(messages);

    // Create or update summary
    const summaryData: InsertSummary = {
      threadId,
      tenantId,
      keyPoints,
      actionItems,
      overallSummary: summaryText,
      messageCount: messages.length,
      generatedAt: new Date().toISOString(),
      lastUpdated: new Date().toISOString(),
      isAutoGenerated: true,
      modelUsed: "mock-summary-model",
      metadata: {
        forceRegenerate,
        processingTime: Date.now() - job.createdAt.getTime(),
      },
    };

    await db.transaction(async (tx) => {
      // Deactivate old summary if exists
      if (!forceRegenerate) {
        await tx
          .update(summariesTable)
          .set({ isAutoGenerated: false })
          .where(eq(summariesTable.threadId, threadId));
      }

      // Create new summary
      const [summary] = await tx
        .insert(summariesTable)
        .values(summaryData)
        .returning();

      // Update thread with summary reference
      await tx
        .update(chatThreadsTable)
        .set({ 
          summaryId: summary.id,
          updatedAt: new Date().toISOString(),
        })
        .where(eq(chatThreadsTable.id, threadId));
    });
  }

  private async processFeedbackJob(job: MockJob): Promise<void> {
    const { feedbackId, tenantId } = job.data;
    
    // Get feedback details
    const [feedback] = await db
      .select()
      .from(feedbackTable)
      .where(
        and(
          eq(feedbackTable.id, feedbackId),
          eq(feedbackTable.tenantId, tenantId)
        )
      );

    if (!feedback) {
      throw new Error("Feedback not found");
    }

    // Process feedback analytics
    await this.updateFeedbackAnalytics(feedback);
  }

  private async generateMockSummary(messages: any[]): Promise<string> {
    // Mock summary generation
    const messageContents = messages
      .filter(m => m.role === "user" || m.role === "assistant")
      .map(m => `${m.role}: ${m.content}`)
      .join("\n");

    return `This conversation contains ${messages.length} messages discussing various topics. Key themes include collaboration, project management, and technical implementation. The participants exchanged ideas and reached several conclusions about the project direction.`;
  }

  private extractKeyPoints(messages: any[]): string[] {
    // Mock key point extraction
    return [
      "Discussed project requirements and specifications",
      "Identified key technical challenges and solutions",
      "Established timeline and milestones",
      "Defined roles and responsibilities",
    ];
  }

  private extractActionItems(messages: any[]): string[] {
    // Mock action item extraction
    return [
      "Review and finalize technical specifications",
      "Schedule follow-up meeting with stakeholders",
      "Prepare implementation plan for next phase",
      "Document decisions and action items",
    ];
  }

  private async updateFeedbackAnalytics(feedback: any): Promise<void> {
    // Mock feedback processing - in a real implementation, this would:
    // - Update sentiment analysis
    // - Track feedback trends
    // - Trigger alerts for negative feedback
    // - Update model performance metrics
    console.log(`Processing feedback ${feedback.id}: ${feedback.rating} - ${feedback.category}`);
  }
}

const jobQueue = new MockJobQueue();

/**
 * Create a summary generation job
 */
export async function createSummaryJob(
  threadId: string,
  forceRegenerate: boolean,
  tenantId: string
): Promise<{ jobId: string; status: string; estimatedCompletion?: string }> {
  try {
    const { jobId } = await jobQueue.add("generate-summary", {
      threadId,
      forceRegenerate,
      tenantId,
    });

    return {
      jobId,
      status: "pending",
      estimatedCompletion: new Date(Date.now() + 30000).toISOString(), // 30 seconds
    };
  } catch (error) {
    throw ErrorTypes.InternalError("Failed to create summary job", error);
  }
}

/**
 * Create a feedback processing job
 */
export async function createFeedbackJob(
  feedbackId: string,
  tenantId: string
): Promise<{ jobId: string; status: string }> {
  try {
    const { jobId } = await jobQueue.add("process-feedback", {
      feedbackId,
      tenantId,
    });

    return {
      jobId,
      status: "pending",
    };
  } catch (error) {
    throw ErrorTypes.InternalError("Failed to create feedback job", error);
  }
}

/**
 * Get job status
 */
export async function getJobStatus(jobId: string): Promise<{
  jobId: string;
  status: string;
  createdAt: string;
  processedAt?: string;
  error?: string;
}> {
  try {
    const job = await jobQueue.getJob(jobId);
    
    if (!job) {
      throw ErrorTypes.NotFound("Job");
    }

    return {
      jobId: job.id,
      status: job.status,
      createdAt: job.createdAt.toISOString(),
      processedAt: job.processedAt?.toISOString(),
      error: job.error,
    };
  } catch (error) {
    if (error instanceof AppError) {
      throw error;
    }
    throw ErrorTypes.InternalError("Failed to get job status", error);
  }
}

